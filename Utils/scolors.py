from pystyle import Colors, Colorate


# ===== Color Definitions ===== #
def purple(text):
    """Prints Green Text

    Args:
        text (String): Text to be Printed

    Returns:
        Print: String
    """
    return f'{Colorate.Color(Colors.purple, f"{text}", True)}'


